class FCN8sPac(nn.Module):
    def __init__(self, n_class=21):
        super(FCN8sPac, self).__init__()
        # conv1
        self.conv1_1 = nn.Conv2d(3, 64, 3, padding=1)
        self.relu1_1 = nn.ReLU(inplace=True)
